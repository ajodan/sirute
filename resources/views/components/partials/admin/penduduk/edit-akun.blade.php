    function showEdit(e) {
        const formEdit = document.getElementById('form-edit-akun');
        for (let input of formEdit) {
            const key = input.name;
            const value = e[key];
            if (value) {
                input.value = value;
            } else if (e.alamat && e.alamat[key]) {
                input.value = e.alamat[key];
            }
        }
    }
</script>
